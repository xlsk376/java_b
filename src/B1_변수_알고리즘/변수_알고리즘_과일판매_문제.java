package B1_변수_알고리즘;

public class 변수_알고리즘_과일판매_문제 {
	public static void main(String[] args) {	
	
		/*
		   [문제]
		 - 가게에 과일 250개가 있다. 
		 - 그중에 오전에 120개가 팔렸고 오후에는  80개가 팔렸다.
		 - 남은 과일는 전체 과일의 몇 % 인가?
		 */
		
		
		double a = 250;
		double b = 120;
		double c = 80;
		
		System.out.println((a - b - c)/a*100);

			
     }
}