package B2_비교연산자_프로젝트;

public class 비교연산자_프로젝트_경주_문제 {

	public static void main(String[] args) {
		/*
		 * [문제]
		 * 자동차는 154km 의 거리를 1시간18분에 달린다.
		 * 트럭은 215km 의 거리를 2시간17분에 달린다.
		 * 자동차가 트럭보다 빠른가요?
		 */
		
		double 자동차 = 154.0/68;
		double 트럭 = 215.0/137;
		
		System.out.println(자동차);
		System.out.println(트럭);
		System.out.println(자동차 > 트럭);

	}

}
